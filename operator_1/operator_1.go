package main

import "fmt"

func main(){
	a := 20
	b := 10

	fmt.Println("Hasil penjumlahan a dan b adalah : ", a + b)
	fmt.Println("Hasil pengurangan a dan b adalah : ", a - b)
	fmt.Println("Hasil perkalian a dan b adalah : ", a * b)
	fmt.Println("Hasil pembagian a dan b adalah : ", a / b)
	fmt.Println("Hasil modulus a dan b adalah : ", a % b)
}